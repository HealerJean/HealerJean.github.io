package co.hlj.domain;

public class Users {
	private String username;
	private String photoBefore;
	private String photoNow;
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getPhotoBefore() {
		return photoBefore;
	}
	public void setPhotoBefore(String photoBefore) {
		this.photoBefore = photoBefore;
	}
	public String getPhotoNow() {
		return photoNow;
	}
	public void setPhotoNow(String photoNow) {
		this.photoNow = photoNow;
	}

}
